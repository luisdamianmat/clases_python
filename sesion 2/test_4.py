"""Listas en  Python"""

"""Listas len(): Obtener el tamaño de una lista en python"""

paises = []

print("El tamaño de mi variable es: {}".format(len(paises)))

#Usando append() parar agregar datos a mi lista

paises.append("Brasil")
paises.append("Perú")
paises.append("España")

print("Los valores de mi lista son: {}".format(paises))
print("El tamaño actual de mi lista es: {}".format(len(paises)))