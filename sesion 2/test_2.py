"""Diccionarios en Python"""

"""Los nombres de los key van  air escritos siempre en minúsuculas y sin tilde (Por convención)"""

var_1 = {"nombre": "Margaret", "edad": 27, "promedio": 16}

print("Nuestro primer diccionario tiene el siguiente contenido: {}".format(var_1))

"""Agregamos elementos en un nuevo key a mi diccionario"""

var_1["distrito"] = "Lince"
var_1["hijo"] = True

print("Mi diccionario actualizado tiene los siguiente valores: {}".format(var_1))